    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $products = Product::with('type')->get();

        if ($request->ajax())
        {
            $order = $request->get('order');
            return view('products.products', compact('products','order'));
        }
        else
            return view('products.index', compact('products'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $types = Type::all()->sortBy("name");
        return view('products.create', compact('types'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product = new Product;
        $product->name = $request->get('name');
        $type = Type::find($request->get('type'));
        $type->products()->save($product);

        //flashy()->success('Nous vous rÃ©pondrons dans les plus bref delais');
        return redirect()->route('product.index'); 
    }

